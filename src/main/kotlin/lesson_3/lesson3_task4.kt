package org.example.lesson_3

fun main() {
    var startOfTheMove: String = "E2"
    var endOfStroke: String = "E4"
    var moveNumber: Int = 1

    println("$startOfTheMove - $endOfStroke; $moveNumber")

    startOfTheMove = "D2"
    endOfStroke = "D3"
    moveNumber = 2

    println("$startOfTheMove - $endOfStroke; $moveNumber")
}